package filipe.ferreira.dto;

public class ChartErroDTO {
	private int pendentes;
	private int analisando;
	private int resolvidos;
	
	
	public int getPendentes() {
		return pendentes;
	}
	public void setPendentes(int pendentes) {
		this.pendentes = pendentes;
	}
	public int getAnalisando() {
		return analisando;
	}
	public void setAnalisando(int analisando) {
		this.analisando = analisando;
	}
	public int getResolvidos() {
		return resolvidos;
	}
	public void setResolvidos(int resolvidos) {
		this.resolvidos = resolvidos;
	}
	
	

}
